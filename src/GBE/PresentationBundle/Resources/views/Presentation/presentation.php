	<p style="text-indent:40px; margin-left : 20px; margin-right : 20px; text-align : justify;">
		Depuis 1958, Enfants du M&eacute;kong aide les enfants pauvres d’Asie du Sud-est  (Vietnam, Thailande, Laos, Birmanie, Cambodge, Chine (Yunnan) et Philippines) autour d’une mission : Eduquer, former et accompagner les enfants et les jeunes, afin de leur permettre d'am&eacute;liorer leurs conditions de vie mat&eacute;rielles et de se construire intellectuellement, affectivement et moralement.
	</p>
	<p style="text-indent:40px; margin-left : 20px; margin-right : 20px; text-align : justify;">
